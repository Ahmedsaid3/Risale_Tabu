package com.ahmedsaidgulsen.risaletabu;

public class WordGroup {

    String mainWord;
    String tabuWord1;
    String tabuWord2;
    String tabuWord3;

    public WordGroup(String mainWord, String tabuWord1, String tabuWord2, String tabuWord3) {
        this.mainWord = mainWord;
        this.tabuWord1 = tabuWord1;
        this.tabuWord2 = tabuWord2;
        this.tabuWord3 = tabuWord3;
    }
}
